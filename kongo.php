                <h2>Historia</h2>
                <p>
                <br />
                Pierwotne ślady pobytu człowieka w Kongu datowane są już od okresu paleolitu. Miedzy V a X wiekiem n.e. zaczęły powstawać pierwsze państwa plemienne (m.in. Bakuba, Baluba, Loanga). Od XIVw. większość z tych królestw (w zach. części dzisiejszej Demokr. Rep. Konga) stopniowo była podporządkowywana średniowiecznemu państwu Kongo (utworzone w XIVw. w dolnym biegu rzeki Kongo przez lut Bakongo).<br /> Okres dominacji państwa Kongo przypadł na XV-XVIIIw. w płd-zach. części obecnego terytorium; niezależnie od niego na południu w okresie XVI-XVIIw. rozwijały się państwa Kuba, Luba oraz Lunda. Wszelako to właśnie Kongo było tym krajem, który jako pierwszy rozpoczął kontakty handlowe z kolonizatorami. W 1482r. przybyli Portugalczycy, którzy rozpoczęli penetrację kraju oraz zakładali misje chrystianizacyjne w dorzeczu Konga.<br /> W 1491r. król Konga, a tym samym cały kraj, przyjął chrześcijaństwo. Wszczęto ożywione kontakty handlowe- głównie pośrednictwo w handlu niewolnikami. Jednak w końcu XVIw. a następnie w ciągu całego XVIIw. wybuchały masowe powstania przeciwko władzy portugalskiej (Kongo poniosło klęskę m.in. w bitwie pod Mbwila w 1665r.). Osłabione wojnami państwo, upadło ostatecznie pod najazdami koczowników oraz przez wzrastającą penetrację europejską (w XVIIIw. głównie holenderską), na przełomie XVIII i XIXw. rozpadając się na wiele zwalczających się królestw (w 1883r. część terytorium przyłączono do portugalskiej kolonii Angola). 
                </p>
