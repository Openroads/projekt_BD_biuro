        <p>Czarna Afryka- Kongo- Krew w dżungli
                
                Procesy dekolonizacyjne, które miały miejsce w krajach Trzeciego Świata były bezpośrednim następstwem drugiej wojny światowej, kiedy to państwa kolonialne (m.in. W.Brytania, Francja, Holandia, Belgia) wycieńczone działaniami wojennymi, nie były w stanie zapanować nad ruchami niepodległościowymi w swoich koloniach i chcąc nie chcąc musiały ustępować z wcześniej zajętych pozycji polityczno-gospodarczych, czego następstwem była niepodległość kolonii. Różnie to przebiegało w poszczególnych krajach- Brytyjczycy mieli dosyć wojen i krwi, Francuzi i Holendrzy tak łatwo nie ustępowali.<br /> Apogeum dekolonizacji przypadło na lata 50-te i 60-te, a sam proces zakończył się w latach 70-tych, kiedy to Portugalia uznała niepodległość Angoli i Mozambikowi. W wielu przypadkach nie była to jednak wolność, która automatycznie uniezależniała od wpływów polityczno-gospodarczych byłych potęg kolonialnych. Dotyczyło to w szczególności kontynentu afrykańskiego, gdzie młode państwa jeszcze długo, długo po uzyskaniu niepodległości były uzależnione od wpływu obcego kapitału i polityki, a w wypadku wielu krajów ten wpływ trwa do dzisiaj. Jednym z państw afrykańskich, które po uzyskaniu niepodległości stało się areną wielkiej rozgrywki politycznej było Kongo Belgijskie.<br /> W początku lat 60-tych wybuchła tam krwawa wojna domowa, a w ów konflikt zostały zaangażowane wojska ONZ oraz Belgii, a pośrednio również dwaj "nowi kolonialiści", czyli USA i ZSRR, czyniąc kontynent afrykański kolejnym polem szachowym w swojej zimnowojennej rozgrywce. Bo też Kongo Belgijskie( ob. Demokratyczna Republika Konga, wcześniej Zair)-kraj miedzią i diamentami płynący- było zbyt łakomym kąskiem dla wielkich tego świata, aby można było pozostawić je samemu sobie. Co spowodowało, że ów kraj stał się w latach 60-tych areną krwawego konfliktu zbrojnego, w który zaangażowane były również kraje tak odległe, jak Belgia czy Stany Zjednoczone? Kluczem do całej wiedzy jest historia... 
                
                
                
