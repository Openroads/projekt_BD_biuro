        <BR /><BR /><BR /><h1><span class="mw-headline" id="Budowa_geologiczna">KOREA PÓŁNOCNA</span></h1><BR /><BR /><BR />
<p>Jeżeli możecie, wybierzcie się do Korei Północnej. Jest tam bardzo, bardzo dziwnie. A jeśli pojedziecie w styczniu, nie tylko dziwnie, ale i bardzo, bardzo zimno. Choć sporo czytałam i słyszałam o tym kraju, nie byłam na to przygotowana. Jakieś -10, -12 stopni Celsjusza, i to w słońcu, a nie w podmuchach lodowatego wiatru. Co gorsza, żaden ze zwiedzanych przez nas budynków nie miał ogrzewania.

Kwatery na każdą kieszeń. Ceny od 15 zł za osobę


Jako goście honorowi mogliśmy liczyć w takich warunkach na niezwykłe doznania: oni pokazują nam swoje najnowsze technologie lub najlepszą bibliotekę, a my widzimy własny oddech. Wygląda na to, że jednak nie wszystko mają pod kontrolą.

Nie sposób stwierdzić, w jakim stopniu to, co zobaczyliśmy w Pjongjangu (Phenianie), oddaje rzeczywistą sytuację w całej Koreańskiej Republice Ludowo-Demokratycznej.

Podczas naszej wizyty nie zabrakło ustawionych spotkań, starannie wyreżyserowanych pokazów, ale i akcentów, które wydawały się całkiem ludzkie i autentyczne. Mimo to nie mieliśmy okazji porozmawiać z żadnymi mieszkańcami, którzy nie zostali wytypowani przez władze, a dwaj "pilnowacze" (dwaj, żeby mogli też pilnować siebie nawzajem) nie odstępowali nas na krok.

Byłam też naprawdę zaskoczona, że funkcjonariusze najbardziej zamkniętego państwa świata wysłuchali (takie przynajmniej sprawiali wrażenie) mnie, nastolatki, gdy opowiadałam im o zaletach internetu. Przeciętni Koreańczycy z Północy żyją w izolowanej od informacji bańce i nie mają żadnego zewnętrznego punktu odniesienia.

Gdy o tym myślę, ogarnia mnie szczere współczucie. Mieszkańcy Korei Północnej, od małego poddawani praniu mózgu, zapewne wierzą, że mają wielkie szczęście, żyjąc w tym kraju.



<br /><br /><br />
<h2>Niezapomniana przygoda</h2>
<p>
<br />
Nic dziwnego więc, że nie chcą stąd wyjeżdżać. Są niewolnikami w swej własnej ojczyźnie, choć zupełnie nie zdają sobie z tego sprawy. Niejasne wewnętrzne mechanizmy funkcjonowania państwa – w tym także podstaw gospodarki – jedynie wzmacniają kontrolę aparatu władzy. Najlepsze porównanie, jakie przychodzi mi na myśl, to film "Truman Show", tyle że na ogólnonarodową skalę.

Nasza delegacja liczyła łącznie dziewięć osób. Bill Richardson, ambasador USA w ONZ oraz wytrawny specjalista od zakulisowej dyplomacji, wybrał się już ósmy raz do KRLD. To on zaprosił mojego ojca Erica Schmidta, prezesa zarządu Google, który z kolei spytał mnie, czy nie chciałabym mu towarzyszyć.

Przedstawiciele chińskich mediów pożegnali nas na lotnisku w Pekinie. Polecieliśmy samolotem linii Air China, pełnym chińskich biznesmenów, działaczy zachodnich organizacji pozarządowych oraz różnej maści dyplomatów. Wszyscy wyglądali na zahartowanych w boju.

Telefony i laptopy zostawiliśmy w Chinach, ostrzeżono nas bowiem, że zostałyby skonfiskowane i zarażone Bóg wie jak złośliwym oprogramowaniem. Wzięliśmy za to ze sobą mnóstwo gotówki (amerykańskich dolarów), jako że jest to właściwie jedyny akceptowany na miejscu środek płatniczy.

Na stanowisku odprawy w Pekinie odebraliśmy wizy – kawałki papieru z przyklejonymi taśmą zdjęciami. Zabrano je nam zaraz po przybyciu do Korei Północnej.

Nasz samolot był jedynym przylatującym tego dnia do Pjongjangu. Na lotnisku czekali nasi opiekunowie, dwaj mężczyźni z tamtejszego ministerstwa spraw zagranicznych.

O dziwo, obaj mieszkali wcześniej w Ameryce oraz innych krajach, pracując w ambasadach. Kłóciło się to trochę ze stereotypem "hermetycznie zamkniętych" Koreańczyków z Północy. Czy oznaczało to, że przeszli oni najtrudniejszą próbę lojalności? Że są jeszcze bardziej ideologicznie zaangażowani niż większość ich rodaków? Jak zatem tłumaczyli sobie dysonans między tym, czym zawsze karmiła ich propaganda, a tym, co zobaczyli za granicą?

Tak czy inaczej, jako "pilnowacze" zachowywali się całkiem w porządku. Byli życzliwi, ale często odpowiadali na nasze pytania wymijająco – lub milczeniem. Chciałabym wierzyć, że trochę nas polubili, choć prawdopodobnie byli jeszcze bardziej zadowoleni, kiedy nas pożegnali.
</p>
