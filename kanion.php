        <BR /><BR /><BR /><h1><span class="mw-headline" id="Budowa_geologiczna">WIELKI KANION</span></h1><BR /><BR /><BR />
 		<p><b>Wielki Kanion</b>, <b>Wielki Kanion Kolorado</b> przełom rzeki Kolorado w stanie Arizona w USA przez Płaskowyż Kolorado. W obecnym kształcie Wielki Kanion ma 446 km długości i osiąga 		         głębokość (<i>Granite Gorge</i> – Wąwóz Granitowy) do około 1600 m. Szerokość waha się od ok. 800 m (pod punktem widokowym Toroweap na <i>North Rim</i> – Północnej Krawędzi) do 29 km w         najszerszym miejscu. Jest to największy przełom rzeki na świecie (jednak nie najgłębszy).</p>
         <p>Obszar ten stanowi park narodowy – <i>Grand Canyon National Park</i>.</p>
		<BR /><BR />
		<h2><span class="mw-headline" id="Budowa_geologiczna">Budowa geologiczna</span></h2>
		<BR />
            <p>Kanion wykształcony jest głównie w łupkach, a także iłołupkach, wapieniach, piaskowcach i granitowych intruzjach. W kanionie tym można dostrzec przekrój geologiczny od proterozoiku (w              prekambrze) po trias(era mezozoiczna). Każda seria warstw skalnych ma inny odcień. Skały osadowe kanionu obfitują w skamieniałości, od pierwotnych glonów do drzew, od morskich muszli po              szczątki dinozaurów.</p>
            <p>Istnieją różne hipotezy na temat powstania kanionu. Najbardziej rozpowszechniona mówi o tym, że proces powstawania był wydłużony w czasie, Według niej kanion zaczął powstawać w              zachodniej części obecnego przebiegu 17 mln lat temu, natomiast wschodni kraniec ukształtował się 5-6 mln lat temu. W następnych milionach lat trwało wcinanie się wgłębne rzeki i              pogłębianie kanionu, przy zachowaniu jego długości. Według tej wersji rzeka Kolorado żłobiła kanion w miarę jak płaskowyż Kolorado się podnosił. Inna wersja mówi o tym, że cały kanion              ukształtował się w przeciągu bardzo krótkiego czasu, nawet kilku dni.
            <p>Przed wybudowaniem w 1963 r. zapory wodnej Glen Canyon rzeka niosła przez kanion dziennie 500 000 ton osadu, obecnie zaś 80 000 ton.</p>
